
public class Proizvodi {
	

	private int hleb;
	private int mleko;
	private int kafa;
	
	public Proizvodi() {
		super();
		this.hleb = hleb;
		this.mleko = mleko;
		this.kafa = kafa;
	}

	public int getHleb() {
		setHleb(hleb);
		return hleb;
	}

	public void setHleb(int hleb) {
		this.hleb = 50;
	}

	public int getMleko() {
		setMleko(mleko);
		return mleko;
	}

	public void setMleko(int mleko) {
		this.mleko = 80;
	}

	public int getKafa() {
		setKafa(kafa);
		return kafa;
	}

	public void setKafa(int kafa) {
		this.kafa = 130;
	}
	
	
	
	

}
